package com.technothinksup.consciouskitchen.model;

public class AppImageResponse {
    private int app_image_id;
    private String app_image_screen;
    private String app_image_image;
    private String app_image_details;

    public int getApp_image_id() {
        return app_image_id;
    }

    public void setApp_image_id(int app_image_id) {
        this.app_image_id = app_image_id;
    }

    public String getApp_image_screen() {
        return app_image_screen;
    }

    public void setApp_image_screen(String app_image_screen) {
        this.app_image_screen = app_image_screen;
    }

    public String getApp_image_image() {
        return app_image_image;
    }

    public void setApp_image_image(String app_image_image) {
        this.app_image_image = app_image_image;
    }

    public String getApp_image_details() {
        return app_image_details;
    }

    public void setApp_image_details(String app_image_details) {
        this.app_image_details = app_image_details;
    }
}
